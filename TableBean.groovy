class TableBean {
    
    private int id
    private String name
    private String city
    
    int getId(){
        return this.id
    }
    
    void setId(int id){
        this.id = id
    }
    
    String getName(){
        return this.name
    }
    
    void setName(String name){
        this.name = name
    }
    
    String getCity(){
        return this.city
    }
    
    void setCity(String city){
        this.city = city
    }
}
